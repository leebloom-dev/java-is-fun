package practice.model;

public class StudentModel {

    /* FIELDS */
    private String name;
    private String roll;

    /* METHODS: GETTERS AND SETTERS */
    public String getName() {
        return name;
    }

    public void setName(String aName) {
        this.name = aName;
    }

    public String getRoll() {
        return roll;
    }

    public void setRoll(String aRoll) {
        this.roll = aRoll;  
    }

}
